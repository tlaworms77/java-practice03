package prob1;

public class Member {
	private String id;
	private String name;
	private int point;
	
	// 회원의 이름 변경
	public void setName(String id) {
		this.id = id;
	}
	// 회원의 이름 반환
	public String getName() {
		return this.name;
	}
	// 포인트 변경
	public void setPoint(int point) {
		this.point = point;
	}
	// 아이디 변경
	public void setId(String id) {
		this.id = id;
	}
	// 아이디 반환
	public String getId() {
		return this.id;
	}
}
